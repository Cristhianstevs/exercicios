// Aula 5: Laços de Repetição

// Tipos de Laços
// - for
// - while
// - do while

// for

for (let i = 0; i < 5; i++) {
    console.log(i);
};

// while 

let contador = 0;

while (contador < 5) {
    console.log(contador);
    contador++
};

// do while

let numero = 10;
do {
    console.log(numero);
    numero--
}
while (numero > 5);