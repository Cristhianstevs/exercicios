// Aula 1 - Variáveis

// Existem 3 tipos de variáveis em JavaScript, são elas:
// var / let / const

// let

let idade = 17;
idade = 18; // Pode ser alterada 
console.log(idade); // Irá mostrar 18 pois foi alterada


// const

const pi = 3.14;
// pi = 3.1415;    // Não vai funcionar, pois o valor de const não pode ser alterada
console.log(pi)


// var

var nome = "Cristhian";
nome = "Esteves";
console.log(nome);